let currentIndex = 0;
let currentSet = null;

const cardText = document.getElementById("card-text");
const prevButton = document.getElementById("prev-button");
const nextButton = document.getElementById("next-button");
const set1Button = document.getElementById("set1-button");
const set2Button = document.getElementById("set2-button");

// Function to update the card text
function updateCard() {
    if (currentSet) {
        cardText.innerHTML = currentSet[currentIndex];
        prevButton.style.display = "block";
        nextButton.style.display = "block"; 
    } else {
        cardText.innerHTML = `
        <div style="text-align: center;">
                <img src="icons/1st.png" alt="Welcome Image" style="width:100%; height:auto; min-height: 400px;">
            <button id="set1-button">أذكار الصباح</button>
            <button id="set2-button">أذكار المساء</button>
        `;
        prevButton.style.display = "none";
        nextButton.style.display = "none";
        document.getElementById("set1-button").addEventListener("click", () => {
            currentSet = flashcardsSet1;
            currentIndex = 0;
            updateCard();
        });
        document.getElementById("set2-button").addEventListener("click", () => {
            currentSet = flashcardsSet2;
            currentIndex = 0;
            updateCard();
        });
    }
}

prevButton.addEventListener("click", () => {
    if (currentSet) {
        if (currentIndex > 0) {
            currentIndex--;
        } else {
            currentIndex = currentSet.length - 1; // Go to the last card when at the first card
        }
        updateCard();
    }
});

nextButton.addEventListener("click", () => {
    if (currentSet) {
        if (currentIndex < currentSet.length - 1) {
            currentIndex++;
        } else {
            currentIndex = 0; // Go back to the first card when the last card is reached
        }
        updateCard();
    }
});

// Initialize the first card
updateCard();